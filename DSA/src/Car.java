
public class Car {
	// Instance variables
	private String make;
	private String model;
	private int Year;
	
	public Car(String make, String model, int Year ) {
		this.make=make;
		this.model=model;
		this.Year=Year;
	}
	
	public void displayDetails() {
		System.out.println("make"+make+"model"+model+"year"+Year);
	}
	

}
	
